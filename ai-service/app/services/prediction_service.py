"""
Prediction Service – predicts which topics are likely to appear in upcoming exams.

Loads a JSON knowledge base generated by the K-Means clustering script
(train_topic_predictor.py).
"""

import os
import json
from typing import List, Dict, Any

MODEL_PATH = os.path.join(os.path.dirname(__file__), "..", "models", "topic_predictor", "topic_predictions.json")

def available_subjects() -> List[str]:
    """Subjects that have trained prediction data (the keys of the predictions JSON)."""
    if not os.path.exists(MODEL_PATH):
        return []
    try:
        with open(MODEL_PATH, "r", encoding="utf-8") as f:
            predictions_db = json.load(f)
        return sorted(predictions_db.keys())
    except Exception as e:
        print(f"[WARN] Failed to read prediction subjects: {str(e)}")
        return []


def predict_topics(subject: str, year: int = 2025) -> List[Dict[str, Any]]:
    """
    Predict topics likely to appear in the next exam for a given subject.

    Args:
        subject: Subject name to predict topics for
        year: Target year for prediction

    Returns:
        List of topic prediction dicts with confidence scores
    """
    # Fallback default if clustering hasn't run yet or subject not found
    default_prediction = [
        {"topic": "General Topics", "confidence": 0.50, "predicted_next_year": True}
    ]

    if not os.path.exists(MODEL_PATH):
        print(f"[WARN] Topic prediction JSON not found at {MODEL_PATH}. Returning fallback.")
        return default_prediction

    try:
        with open(MODEL_PATH, "r", encoding="utf-8") as f:
            predictions_db = json.load(f)
            
        return predictions_db.get(subject, default_prediction)
    except Exception as e:
        print(f"[ERROR] Failed to load topic predictions: {str(e)}")
        return default_prediction
